import { newId } from './crypto';
import type { AuthContext, Env, WorldSummary } from './types';

type Language = 'en' | 'zh';

interface WorldRow {
  id: string;
  name: string;
  language: Language;
  story_day: number;
  created_at: string;
  updated_at: string;
  auto_advance_enabled: number;
  character_count: number;
  memory_count: number;
  story_count: number;
}

export interface WorldExportData {
  format: 'ai-town-lite-world';
  version: 1;
  exportedAt: string;
  source: {
    worldName: string;
    storyDay: number;
    language: Language;
  };
  world: {
    name: string;
    language: Language;
    storyDay: number;
  };
  characters: Array<{
    id: string;
    name: string;
    gender: string;
    personality: string;
    appearance: string;
    backstory: string;
    isAutoGenerated: boolean;
  }>;
  memories: Array<{
    id: string;
    characterId: string | null;
    content: string;
    source: string;
    importance: number;
  }>;
  lexiconEntries: Array<{
    id: string;
    term: string;
    description: string;
    language: 'en' | 'zh' | 'both';
  }>;
  stories: Array<{
    id?: string;
    dayNumber: number;
    title: string;
    content: string;
    language: Language;
    model: string;
    promptHash: string;
    beats?: Array<{
      beatIndex: number;
      timeSlot: string;
      location: string;
      mood: string;
      event: string;
      dialogue: string[];
      participatingCharacters: string[];
      memoryImpact: string;
    }>;
  }>;
}

