export function makeTranslator(language: Language): Translator {
  return (key) => messages[language][key];
}

export function localizeStaticText(value: string, language: Language): string {
  const pair = localTextPairs.find(([en, zh]) => value === en || value === zh);
  if (!pair) return value;
  return language === 'zh' ? pair[1] : pair[0];
}

export function localizeCharacter(character: CharacterRecord, language: Language): CharacterRecord {
  return {
    ...character,
    gender: localizeStaticText(character.gender, language),
    personality: localizeStaticText(character.personality, language),
    appearance: localizeStaticText(character.appearance, language),
    backstory: localizeStaticText(character.backstory, language),
  };
}
